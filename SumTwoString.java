package com.string.question;

public class SumTwoString {
	public static void main(String[] args) {
		String str1 = "111";
		String str2 = "222";
		int sum = Integer.parseInt(str1) + Integer.parseInt(str2);
		System.out.println("Addition of string is: " + sum);
	}
}
// output-
// Addition of string is: 333
